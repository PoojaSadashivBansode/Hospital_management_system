    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try 
        {
            // Get the form data
            int staffID = Integer.parseInt(t1.getText());  
            String staffName = t2.getText();              
            String role = (String) t3.getSelectedItem();  
            String contactNo = t4.getText();               
            double salary = Double.parseDouble(t5.getText()); 
            String qualification = t6.getText();           
            String experience = t7.getText();   
            String availability = t8.isSelected() ? "Full Time" : "Part Time";  
            // Load MySQL Driver
            Class.forName("com.mysql.cj.jdbc.Driver");

            // Connect to the database
            String url = "jdbc:mysql://localhost/hospital";  // Replace with your DB name
            Connection cn = DriverManager.getConnection(url, "root", "pooja2411");  // Replace with your DB credentials

            // Prepare SQL Query
            String query = "INSERT INTO StaffDetails (StaffID, Name, Role, ContactNo, Salary, Qualification, Experience, Availability) " +"VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = cn.prepareStatement(query);

            // Set the values for the prepared statement
            pst.setInt(1, staffID);
            pst.setString(2, staffName);
            pst.setString(3, role);
            pst.setString(4, contactNo);
            pst.setDouble(5, salary);
            pst.setString(6, qualification);
            pst.setString(7, experience);
            pst.setString(8, availability);

            // Execute the update
            pst.executeUpdate();
            System.out.println("Staff details inserted successfully!");

            // Close the connection
            pst.close();
            cn.close();
        } 
        catch (Exception e)
        {
            System.out.println(e);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

