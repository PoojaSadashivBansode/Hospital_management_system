    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try 
        {
            int patientID = Integer.parseInt(t1.getText());     
            String patientName = t2.getText();    
            JCalendar jCalendar = new JCalendar();
            Date selectedDate = jCalendar.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String formattedDate = sdf.format(selectedDate);                       
            String testType = (String) ComboBox.getSelectedItem(); 
            String status = statusPositive.isSelected() ? "Positive" : "Negative";  
            Class.forName("com.mysql.cj.jdbc.Driver");
            String url = "jdbc:mysql://localhost/hospital"; 
            Connection cn = DriverManager.getConnection(url, "root", "pooja2411");

            String query = "INSERT INTO LabTestDetails (PatientID, PatientName, Date, TestType, Status) VALUES (?,?,?,?,?)";
            PreparedStatement pst = cn.prepareStatement(query);
            pst.setInt(1, patientID);
            pst.setString(2, patientName);
            pst.setString(3, formattedDate);
            pst.setString(4, testType);
            pst.setString(5, status);

            pst.executeUpdate();
            System.out.println("Lab test details inserted successfully");

        } 
        catch (Exception e)
        {
            System.out.println(e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

