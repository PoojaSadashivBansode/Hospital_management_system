    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try 
        {
            // Get the form data
            int patientID = Integer.parseInt(t1.getText());
            String name = t2.getText();
            String contactNo = t3.getText();
            String gender = male.isSelected() ? "Male" : "Female";
            int age = Integer.parseInt(t4.getText());
            String bloodGroup = t5.getText();
            String address = t6.getText();
            Class.forName("com.mysql.cj.jdbc.Driver");
            String url = "jdbc:mysql://localhost/hospital";  
            Connection cn = DriverManager.getConnection(url, "root", "pooja2411");  
            String query = "INSERT INTO PatientDetails(PatientID, Name, ContactNo, Gender, Age, BloodGroup, Address) " +"VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = cn.prepareStatement(query);
            pst.setInt(1, patientID);
            pst.setString(2, name);
            pst.setString(3, contactNo);
            pst.setString(4, gender);
            pst.setInt(5, age);
            pst.setString(6, bloodGroup);
            pst.setString(7, address);
            // Execute update
            pst.executeUpdate();
            System.out.println("Data inserted successfully!");
            // Close connection
            pst.close();
            cn.close();
        } 
        catch (Exception e) 
        {
            System.out.println(e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

