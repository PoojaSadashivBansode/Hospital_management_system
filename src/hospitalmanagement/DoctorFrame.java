    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         try {
                    Class.forName("com.mysql.cj.jdbc.Driver");
                    String url = "jdbc:mysql://localhost/hospital";
                    Connection cn = DriverManager.getConnection(url, "root", "pooja2411");
                    Statement st = cn.createStatement();
                    String doctorName = t1.getText();
                    // Query to fetch appointments for the specific doctor
                    String query = "SELECT * FROM appointmentdetails WHERE DoctorName = '" + doctorName + "'";
                    ResultSet rs = st.executeQuery(query);

                    // Getting metadata to determine column count
                    ResultSetMetaData rsmd = rs.getMetaData();
                    int columnCount = rsmd.getColumnCount();

                    // Creating a model for the JTable
                    DefaultTableModel model = new DefaultTableModel();

                    // Adding columns to the model
                    for (int i = 1; i <= columnCount; i++) {
                        model.addColumn(rsmd.getColumnName(i));
                    }

                    // Adding rows to the model
                    while (rs.next()) {
                        Object[] rowData = new Object[columnCount];
                        for (int i = 1; i <= columnCount; i++) {
                            rowData[i - 1] = rs.getObject(i);
                        }
                        model.addRow(rowData);
                    }

                    // Creating a JTable and setting the model
                    JTable table = new JTable(model);
                    JScrollPane scrollPane = new JScrollPane(table);
                    JFrame tableFrame = new JFrame("Appointment Details for " + doctorName);
                    tableFrame.add(scrollPane);
                    tableFrame.setSize(800, 400);
                    tableFrame.setVisible(true);

                } catch (Exception ex) {
                    System.out.println(ex);
                }
    }//GEN-LAST:event_jButton2ActionPerformed

